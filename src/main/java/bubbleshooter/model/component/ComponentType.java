package bubbleshooter.model.component;

/**
 * Enum which contains the type of {@link Component} in the game.
 *
 */
public enum ComponentType {

     /**
     * THE COMPONENT OF THE BUBBLE USED FOR MOVING THE BUBBLE.
     */
    SHOOTINGCOMPONENT,

    /**
     * THE COMPONENT OF THE BUBBLE USED FOR GENERATE THE COLLISION'S SHAPE.
     */
    COLLISIONCOMPONENT;
}
